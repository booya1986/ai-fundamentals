# ── Low-level RTL helpers ─────────────────────────────────────────────────────

def _set_para_rtl(p):
    """Set w:bidi + w:jc right on a paragraph's pPr (idempotent)."""
    pPr = p._p.get_or_add_pPr()
    for tag in ('w:bidi', 'w:jc'):
        for el in pPr.findall(qn(tag)):
            pPr.remove(el)
    bidi = OxmlElement('w:bidi')
    pPr.insert(0, bidi)
    jc = OxmlElement('w:jc')
    jc.set(qn('w:val'), 'right')
    pPr.append(jc)


def _set_run_rtl(run):
    """Set w:rtl on a run's rPr."""
    rPr = run._r.get_or_add_rPr()
    for el in rPr.findall(qn('w:rtl')):
        rPr.remove(el)
    rtl = OxmlElement('w:rtl')
    rPr.append(rtl)
    # Also tell the font-complex-script to use the same face
    rFonts = rPr.find(qn('w:rFonts'))
    if rFonts is None:
        rFonts = OxmlElement('w:rFonts')
        rPr.insert(0, rFonts)
    rFonts.set(qn('w:cs'), run.font.name or FONT_BODY)


def _set_table_rtl(table):
    """Set w:bidiVisual on a table so columns flow right-to-left."""
    tbl = table._tbl
    tblPr = tbl.find(qn('w:tblPr'))
    if tblPr is None:
        tblPr = OxmlElement('w:tblPr')
        tbl.insert(0, tblPr)
    for el in tblPr.findall(qn('w:bidiVisual')):
        tblPr.remove(el)
    bv = OxmlElement('w:bidiVisual')
    tblPr.append(bv)


def _set_cell_bg(cell, hex_color):
    tcPr = cell._tc.get_or_add_tcPr()
    for el in tcPr.findall(qn('w:shd')):
        tcPr.remove(el)
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), hex_color)
    tcPr.append(shd)


def _apply_rtl_to_all_paragraphs(doc_or_cell):
    """Walk every paragraph in a document or cell and apply RTL."""
    paras = (
        doc_or_cell.paragraphs
        if hasattr(doc_or_cell, 'paragraphs')
        else doc_or_cell.paragraphs
    )
    for p in paras:
        _set_para_rtl(p)
        for run in p.runs:
            _set_run_rtl(run)


# ── Document-level setup ──────────────────────────────────────────────────────

def _configure_document(doc):
    """Set page size, margins, and Normal style default to RTL."""
    section = doc.sections[0]
    section.page_width  = Cm(21)
    section.page_height = Cm(29.7)
    section.right_margin  = Cm(2.5)
    section.left_margin   = Cm(2.5)
    section.top_margin    = Cm(2.2)
    section.bottom_margin = Cm(2.2)

    # Set Normal style default to RTL so inherited styles work
    normal = doc.styles['Normal']
    pPr = normal.element.get_or_add_pPr()
    for tag in ('w:bidi', 'w:jc'):
        for el in pPr.findall(qn(tag)):
            pPr.remove(el)
    bidi = OxmlElement('w:bidi')
    pPr.insert(0, bidi)
    jc = OxmlElement('w:jc')
    jc.set(qn('w:val'), 'right')
    pPr.append(jc)

    # Set Normal style run defaults to RTL + Arial
    rPr = normal.element.get_or_add_rPr()
    for el in rPr.findall(qn('w:rtl')):
        rPr.remove(el)
    rtl = OxmlElement('w:rtl')
    rPr.append(rtl)
    rFonts = rPr.find(qn('w:rFonts'))
    if rFonts is None:
        rFonts = OxmlElement('w:rFonts')
        rPr.insert(0, rFonts)
    rFonts.set(qn('w:ascii'),    FONT_BODY)
    rFonts.set(qn('w:hAnsi'),   FONT_BODY)
    rFonts.set(qn('w:cs'),       FONT_BODY)


# ── Content helpers ───────────────────────────────────────────────────────────

def _make_run(p, text, bold=False, italic=False, size=10.5,
              color=DARK, font=FONT_BODY):
    run = p.add_run(text)
    run.bold   = bold
    run.italic = italic
    run.font.size      = Pt(size)
    run.font.name      = font
    run.font.color.rgb = color
    # complex-script size (required for RTL fonts to render at correct size)
    rPr = run._r.get_or_add_rPr()
    sz2 = OxmlElement('w:szCs')
    sz2.set(qn('w:val'), str(int(size * 2)))
    rPr.append(sz2)
    _set_run_rtl(run)
    return run


def add_heading(doc, text, level=1, color=RED):
    p = doc.add_paragraph()
    _set_para_rtl(p)
    p.paragraph_format.space_after  = Pt(4)
    p.paragraph_format.space_before = Pt(8)

    sizes   = {0: 22, 1: 16, 2: 13, 3: 11}
    fonts   = {0: FONT_SANS, 1: FONT_SANS, 2: FONT_SANS, 3: FONT_SANS}
    colors  = {0: color, 1: color, 2: RED_DARK, 3: DARK}
    befores = {0: 12, 1: 18, 2: 14, 3: 10}
    afters  = {0:  6, 1:  6, 2:  4, 3:  2}

    p.paragraph_format.space_before = Pt(befores.get(level, 10))
    p.paragraph_format.space_after  = Pt(afters.get(level, 4))

    run = _make_run(p, text, bold=True,
                    size=sizes.get(level, 11),
                    color=colors.get(level, DARK),
                    font=fonts.get(level, FONT_SANS))

    if level == 1:
        pPr = p._p.get_or_add_pPr()
        pBdr = OxmlElement('w:pBdr')
        bottom = OxmlElement('w:bottom')
        bottom.set(qn('w:val'),   'single')
        bottom.set(qn('w:sz'),    '6')
        bottom.set(qn('w:space'), '1')
        bottom.set(qn('w:color'), 'C0392B')
        pBdr.append(bottom)
        pPr.append(pBdr)
    return p


def add_body(doc, text, bold=False, italic=False, indent=False, size=10.5):
    p = doc.add_paragraph()
    _set_para_rtl(p)
    p.paragraph_format.space_after = Pt(4)
    if indent:
        p.paragraph_format.right_indent = Cm(1)
    _make_run(p, text, bold=bold, italic=italic, size=size)
    return p


def add_bullet(doc, text, level=0):
    """RTL bullet: manual em-dash bullet with right indent."""
    p = doc.add_paragraph()
    _set_para_rtl(p)
    p.paragraph_format.space_after   = Pt(2)
    p.paragraph_format.right_indent  = Cm(0.6 + level * 0.8)
    p.paragraph_format.left_indent   = Cm(0)
    # Hanging indent so the bullet aligns right and text wraps inward
    p.paragraph_format.first_line_indent = Cm(0)
    _make_run(p, f'• {text}', size=10.5)
    return p


def add_note(doc, text):
    p = doc.add_paragraph()
    _set_para_rtl(p)
    p.paragraph_format.space_after = Pt(6)
    _make_run(p, text, italic=True, size=9.5, color=GREY)
    return p


def add_divider(doc):
    p = doc.add_paragraph()
    _set_para_rtl(p)
    pPr = p._p.get_or_add_pPr()
    pBdr = OxmlElement('w:pBdr')
    bottom = OxmlElement('w:bottom')
    bottom.set(qn('w:val'),   'single')
    bottom.set(qn('w:sz'),    '4')
    bottom.set(qn('w:space'), '1')
    bottom.set(qn('w:color'), 'CCCCCC')
    pBdr.append(bottom)
    pPr.append(pBdr)
    p.paragraph_format.space_after = Pt(8)


def page_break(doc):
    doc.add_page_break()


def make_table(doc, headers, rows, col_widths=None, header_bg='C0392B'):
    """Create a fully RTL table."""
    num_cols = len(headers)
    table = doc.add_table(rows=1 + len(rows), cols=num_cols)
    table.style = 'Table Grid'
    # RTL: bidiVisual makes columns flow right → left
    _set_table_rtl(table)
    # Table alignment: in RTL docs the table itself should be left-aligned
    # so it fills the page from the right edge
    table.alignment = WD_TABLE_ALIGNMENT.LEFT

    def _write_cell(cell, text, bold=False, bg=None,
                    txt_color=DARK, font=FONT_BODY, size=9.5):
        if bg:
            _set_cell_bg(cell, bg)
        cell.vertical_alignment = WD_ALIGN_VERTICAL.CENTER
        # Clear existing paragraphs
        for p in cell.paragraphs:
            for run in p.runs:
                run.text = ''
        p = cell.paragraphs[0]
        _set_para_rtl(p)
        p.paragraph_format.space_after  = Pt(2)
        p.paragraph_format.space_before = Pt(2)
        _make_run(p, str(text) if text is not None else '',
                  bold=bold, size=size, color=txt_color, font=font)

    # Header row
    for i, h in enumerate(headers):
        _write_cell(table.rows[0].cells[i], h,
                    bold=True, bg=header_bg, txt_color=WHITE, font=FONT_SANS)

    # Data rows
    for ri, row_data in enumerate(rows):
        bg = 'F5F5F5' if ri % 2 == 0 else 'FFFFFF'
        for ci, val in enumerate(row_data):
            _write_cell(table.rows[ri + 1].cells[ci], val, bg=bg)

    # Column widths
    if col_widths:
        for ci, w in enumerate(col_widths):
            for cell in table.columns[ci].cells:
                cell.width = Cm(w)

    doc.add_paragraph()   # spacing after table
    return table


